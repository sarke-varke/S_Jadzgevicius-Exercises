let pirmas = 10;
let antras = 20;

console.log(pirmas + antras); 
console.log(pirmas - antras); 
console.log(pirmas * antras); 
console.log(pirmas / antras); 
console.log(pirmas % antras); 
console.log(pirmas ** antras); 
console.log(pirmas++);
console.log(++pirmas); 
console.log(pirmas--); 
console.log(--pirmas); 
console.log(--pirmas + antras++); 
console.log(pirmas-- - --antras + antras++); 
console.log(pirmas++ / antras++); 
console.log(pirmas++ * --antras); 
